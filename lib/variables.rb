class VariablesGlobales

=begin
    def initialize(intento,codigo)
        @intentos = intentos
        @codigo = codigo
        
    end
    def getIntentos()
        return @intentos
    end

    def setIntentos(numeroIntentos)
        @intentos = numeroIntentos
        return @intentos
    end

    def descontarIntentos()
        @intentos -= 1
        return @intentos
    end

    def getcodigo() 
        return @codigo
    end

    def setcodigo(numbercodigo)
        @codigo = numbercodigo
        return @codigo
    end
=end
end